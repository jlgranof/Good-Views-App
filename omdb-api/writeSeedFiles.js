        // for (let j = 0; j < movie.genre.length; j++) {
        //     sequelizeStringGenres += `{ movieId: ${i}, genreId: ${movie.genre[j]}, createdAt: new Date(), updatedAt: new Date() }, \n`
        // }
        console.log(i, movie.title)
